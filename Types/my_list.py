# Adding and removing list elements
my_list = [10, "bw"]
print(f'My list: {my_list}')

# Let's append something here
my_list.append("abc")
print(f'After append: {my_list}')

# Now let's pop something here
my_list.pop(1)
print(f'After pop: {my_list}')

# Now let's remove something here
my_list.remove("abc")
print(f'After remove: {my_list}')
